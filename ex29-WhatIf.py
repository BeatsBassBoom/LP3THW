# assigns values to static variables
people = 20
cats = 30
dogs = 15

# declares an if statement for comparing two variables
if people < cats:
    # will print if the if statemnt is true.
    print("Too many cats! The world is doomed!")

if people > cats:
    print("Not many cats! The world is saved!")

if people < dogs:
    print("The world is drooled on!")

if people > dogs:
    print("The world is dry!")

# does addition on the dogs variable.
dogs += 5

# if statement checking for greater than or equals
if people >= dogs:
    print("People are greater than or equal to dogs.")

if people <= dogs:
    print("People are less than or equal to dogs.")


if people == dogs:
    print("People are dogs.")